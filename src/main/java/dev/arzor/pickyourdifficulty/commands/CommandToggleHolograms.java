// ğŸª§ CommandToggleHolograms â€” /pyd toggleholograms
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
public class CommandToggleHolograms implements CommandExecutor {

    // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    // ğŸ“¦ Utilities
    // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

    // ğŸ’¬ MiniMessage parser for deserializing text from messages.yml
    private final MiniMessage mm = MiniMessage.miniMessage();

    // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    // âš™ï¸ Command Execution
    // â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    @Override
    public boolean onCommand(@Nonnull CommandSender sender, @Nonnull Command command, @Nonnull String label, @Nonnull String[] args) {

        // ğŸ“¦ Debug: Toggle command triggered
        PickYourDifficulty.debug("/pyd toggleholograms invoked by: " + sender.getName());

        // â•”â•â•â•ğŸš« Must Be Playerâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•—
        // This command cannot be used from console or command blocks
        if (!(sender instanceof Player player)) {
            PickYourDifficulty.debug("Blocked /pyd toggleholograms â€” sender is not a player.");
            sender.sendMessage(mm.deserialize(MessagesManager.get("toggle.players-only")));
            return true;
        }

        // â•”â•â•â•ğŸ” Permission Checkâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•—
        // Only allow access if permission enforcement is enabled AND player has permission
        if (ConfigManager.requireCommandPermissions() && !PermissionUtil.hasHologramTogglePermission(player)) {
            PickYourDifficulty.debug("Blocked /pyd toggleholograms â€” " + player.getName() + " lacks permission.");

            // ğŸš« Inform sender they don't have permission to use this command
            player.sendMessage(mm.deserialize(MessagesManager.get("toggle.no-permission")));
            return true;
        }

        // â•”â•â•â•âœ… Toggle hologram visibilityâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•—

        // ğŸ”„ Flip visibility for this player
        boolean nowHidden = HologramManager.toggleHidden(player);
        PickYourDifficulty.debug("Toggled hologram visibility for " + player.getName() + " â†’ nowHidden = " + nowHidden);

        // ğŸ“© Determine which message to show
        String messageKey = nowHidden ? "toggle.success-off" : "toggle.success-on";

        // ğŸ“¨ Send updated status message
        player.sendMessage(mm.deserialize(MessagesManager.get(messageKey)));

        return true;
    }
}